-----------------------------------------------------------
-- Filetype Detection
-----------------------------------------------------------

-----------------------------------------------------------
-- cjs
-----------------------------------------------------------
vim.filetype.add {
    pattern = {
        ['*.cjs'] = 'javascript'
    }
}

-----------------------------------------------------------
-- jsonc
-----------------------------------------------------------
vim.filetype.add {
    filename = {
        ['.eslintrc'] = 'jsonc',
        ['.eslintrc.json'] = 'jsonc',
        ['.babelrc'] = 'jsonc',
        ['jsconfig.json'] = 'jsonc',
        ['tsconfig.json'] = 'jsonc',
    },
    pattern = {
        ['.*/.vscode/.*.json'] = 'jsonc',
    },
}
